function mayor(pnumero1, pnumero2){
    let numero1 = pnumero1;
    let numero2 = pnumero2;
    if(numero1 != numero2){
        if(numero1 > numero2){
            return "Número uno mayor: "+numero1;
        }else{
            return "Número dos mayor: "+numero2;
        }
    }else{
        return "Los números son iguales"
    }
}

